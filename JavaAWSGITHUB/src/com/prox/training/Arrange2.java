package com.prox.training;

public class Arrange2 {

		public static void main(String[] args) {
		// Assignment 2-1
		for(int i=1;i<=5;i++)
		{
			for(int j=1;j<=i;j++)
			{
				System.out.print(i + " ");
			}
				System.out.println(" ");
		}
		for(int i=4;i>=1;i--)
		{
			for(int j=i;j>0;j--)
			{
				System.out.print(i + " ");
			}
			System.out.println(" ");
		}
					
	}
}

